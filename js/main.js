let show1 = document.getElementById('show1');
show1.addEventListener('mouseenter', function(){
	let showTop = document.getElementsByClassName('show_top');
	let showLeft = document.getElementsByClassName('show_left');
	let showDown = document.getElementsByClassName('show_down');
	let showRight = document.getElementsByClassName('show_raight');
	showTop[0].style.display = 'flex';
	showLeft[0].style.display = 'flex';
	showDown[0].style.display = 'flex';
	showRight[0].style.display = 'flex';
})
show1.addEventListener('mouseleave', function(){
	let showTop = document.getElementsByClassName('show_top');
	let showLeft = document.getElementsByClassName('show_left');
	let showDown = document.getElementsByClassName('show_down');
	let showRight = document.getElementsByClassName('show_raight');
	showTop[0].style.display = 'none';
	showLeft[0].style.display = 'none';
	showDown[0].style.display = 'none';
	showRight[0].style.display = 'none';
})
